# string slicer

strVar = "Hello"
a = int(input("enter a "))
b = int(input("enter b "))

print(strVar[a])
print(strVar[a:b])
print(strVar[:b])
print(strVar[a:])
print(strVar[-a:-b])
print(len(strVar))
print(strVar.replace("H", "J"))
print(strVar.split("e"))
print("length", len(strVar))
print(strVar.upper())
print(strVar.lower())



